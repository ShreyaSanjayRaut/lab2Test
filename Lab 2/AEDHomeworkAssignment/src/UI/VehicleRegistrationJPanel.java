    private void yearTxKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_yearTxKeyPressed
        // TODO add your handling code here:
         char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            yearTx.setEditable(false);
                 JOptionPane.showMessageDialog(null, "Please entry numbers only.", "Warning", JOptionPane.ERROR_MESSAGE);
        
        } else {
            yearTx.setEditable(true);
        }
    }//GEN-LAST:event_yearTxKeyPressed


