    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        vehicleInfo.setManufacturingYear(yearTx.getText());
        vehicleInfo.setModelName(modelNameTx.getText());
        vehicleInfo.setOwnerName(ownerNameTx.getText());
        vehicleInfo.setRegistrationNumber(registrationNumberTx.getText());
        vehicleInfo.setSerialnNumber(serialNumberTx.getText());
       
        if (registrationNumberTx.getText().isEmpty() || ownerNameTx.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Please enter required fields.", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Vehicle Information Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_SaveButtonActionPerformed

