    private void phoneNumberTxKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_phoneNumberTxKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            phoneNumberTx.setEditable(false);
            JOptionPane.showMessageDialog(null, "Please enter Number only.", "Warning", JOptionPane.ERROR_MESSAGE);
               //int ERROR_MESSAGE = JOptionPane.ERROR_MESSAGE;
        } else {
            phoneNumberTx.setEditable(true);
        }
    }//GEN-LAST:event_phoneNumberTxKeyPressed

