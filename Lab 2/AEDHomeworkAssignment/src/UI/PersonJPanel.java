    private void SavebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SavebtnActionPerformed

        person.setFirstName(firstNametx.getText());
        person.setLastName(lastNameTx.getText());
        try{
        if( !ageTx.getText().isBlank() || !ageTx.getText().isEmpty()){
        if (ageTx.getText().matches("\\d+")) {
        int personAge = Integer.parseInt(ageTx.getText());
        person.setAge(personAge);
        }
        }
        //person.setAge(ageTx.getText());
        person.setPassportNumber(passportTx.getText());
        if( !phoneNumberTx.getText().isBlank() || !phoneNumberTx.getText().isEmpty()){
        if (phoneNumberTx.getText().matches("\\d+")) {
        int personPhoneNumber = Integer.parseInt(phoneNumberTx.getText());
        person.setPhoneNumber(personPhoneNumber);
        }
        }
        }catch(Exception e)
        {
            
        }
        //person.setPhoneNumber(phoneNumberTx.getText());
        person.setSSN(SSNTx.getText());
        person.setOccupation(occupationTx.getText());
        person.setBooldType(bloodTypeTx.getText());
        person.setHobby(hobbyTx.getText());
        person.setEducation(educationPersonTx.getText());
        address.setCity(cityTx.getText());
        address.setCountry(countryTx.getText());
        address.setGPSLocation(GPSLocationTx.getText());
        address.setState(stateTx.getText());
    
        if (firstNametx.getText().isEmpty() || lastNameTx.getText().isEmpty() || SSNTx.getText().isEmpty() || phoneNumberTx.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please entry all the required fields.", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (phoneNumberTx.getText().length() != 10) {
            JOptionPane.showMessageDialog(null, "Phone number should be of 10 digit.", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Person Information Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_SavebtnActionPerformed

